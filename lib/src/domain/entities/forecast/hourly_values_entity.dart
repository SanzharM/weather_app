import 'dart:convert';

import 'package:equatable/equatable.dart';

class HourlyValuesEntity extends Equatable {
  final List<DateTime?> time;
  final List<double?> temperature2m;
  final List<int?> relativeHumidity2m;
  final List<int?> precipitationProbability;
  final List<double?> rain;
  final List<double?> snowfall;
  final List<int?> cloudcover;
  final List<double?> visibility;

  const HourlyValuesEntity({
    this.time = const [],
    this.temperature2m = const [],
    this.relativeHumidity2m = const [],
    this.precipitationProbability = const [],
    this.rain = const [],
    this.snowfall = const [],
    this.cloudcover = const [],
    this.visibility = const [],
  });

  HourlyValuesEntity copyWith({
    List<DateTime?>? time,
    List<double?>? temperature2m,
    List<int?>? relativeHumidity2m,
    List<int?>? precipitationProbability,
    List<double?>? rain,
    List<double?>? snowfall,
    List<int?>? cloudcover,
    List<double?>? visibility,
  }) {
    return HourlyValuesEntity(
      time: time ?? this.time,
      temperature2m: temperature2m ?? this.temperature2m,
      relativeHumidity2m: relativeHumidity2m ?? this.relativeHumidity2m,
      precipitationProbability: precipitationProbability ?? this.precipitationProbability,
      rain: rain ?? this.rain,
      snowfall: snowfall ?? this.snowfall,
      cloudcover: cloudcover ?? this.cloudcover,
      visibility: visibility ?? this.visibility,
    );
  }

  Map<String, dynamic> toMap() {
    return {
      'time': time.map((x) => x?.millisecondsSinceEpoch).toList(),
      'temperature_2m': temperature2m,
      'relativehumidity_2m': relativeHumidity2m,
      'precipitation_probability': precipitationProbability,
      'rain': rain,
      'snowfall': snowfall,
      'cloudcover': cloudcover,
      'visibility': visibility,
    };
  }

  factory HourlyValuesEntity.fromMap(Map<String, dynamic> map) {
    return HourlyValuesEntity(
      time: List<DateTime?>.from(
          map['time']?.map((x) => DateTime.fromMillisecondsSinceEpoch(x))),
      temperature2m: List<double?>.from(map['temperature_2m']),
      relativeHumidity2m: List<int?>.from(map['relativehumidity_2m']),
      precipitationProbability: List<int?>.from(map['precipitation_probability']),
      rain: List<double?>.from(map['rain']),
      snowfall: List<double?>.from(map['snowfall']),
      cloudcover: List<int?>.from(map['cloudcover']),
      visibility: List<double?>.from(map['visibility']),
    );
  }

  String toJson() => json.encode(toMap());

  factory HourlyValuesEntity.fromJson(String source) =>
      HourlyValuesEntity.fromMap(json.decode(source));

  @override
  String toString() {
    return 'HourlyValuesEntity(time: $time, temperature2m: $temperature2m, relativeHumidity2m: $relativeHumidity2m, precipitationProbability: $precipitationProbability, rain: $rain, snowfall: $snowfall, cloudcover: $cloudcover, visibility: $visibility)';
  }

  @override
  List<Object> get props {
    return [
      time,
      temperature2m,
      relativeHumidity2m,
      precipitationProbability,
      rain,
      snowfall,
      cloudcover,
      visibility,
    ];
  }
}

